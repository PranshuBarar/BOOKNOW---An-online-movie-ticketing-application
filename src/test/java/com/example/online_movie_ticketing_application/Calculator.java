public class Calculator {

    //sum
    public int doSum(int a, int b, int c){
        return a + b + c;
    }


    //product
    public int doMult(int a, int b){
        return a * b;
    }

    public boolean compareTwoNums(int a, int b){
        return a==b;
    }
}
